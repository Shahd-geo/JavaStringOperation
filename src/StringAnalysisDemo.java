public class StringAnalysisDemo {
    static void main(String[] args) {
        String MOTIVATION = ("work hard");
        System.out.println(MOTIVATION);
        System.out.println(MOTIVATION.length());
        String MOTIVATION2 = ("well done");
        System.out.println(MOTIVATION.equals(MOTIVATION2));
        System.out.println(MOTIVATION.indexOf("h"));
        System.out.println(MOTIVATION.startsWith("work"));
        System.out.println(MOTIVATION.endsWith("d"));


    }
}
