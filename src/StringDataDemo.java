public class StringDataDemo {
    static void main(String[] args) {
        String MASSAGE = ("GOOD MORNING SUN");
        System.out.println(MASSAGE);
        System.out.println(MASSAGE.toUpperCase());
        System.out.println(MASSAGE.toLowerCase());
        System.out.println(MASSAGE.indexOf("S"));
        System.out.println(MASSAGE.substring(1,6));
        System.out.println(MASSAGE.contains("oo"));
    }
}
